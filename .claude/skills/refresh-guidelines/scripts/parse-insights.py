def parse_facets(facets_dir: Path) -> dict:
    """Aggregate data from facets/*.json files.

    Returns dict with friction_types, session_types, outcomes, summaries.
    """
    result: dict = {
        "friction_types": {},
        "session_types": {},
        "outcomes": {},
        "total_sessions": 0,
        "summaries": [],
    }

    if not facets_dir.exists():
        return result

    for f in sorted(facets_dir.glob("*.json")):
        try:
            data = json.loads(f.read_text(encoding="utf-8"))
        except (json.JSONDecodeError, OSError):
            continue

        result["total_sessions"] += 1

        # Friction types
        for ftype, count in data.get("friction_counts", {}).items():
            result["friction_types"][ftype] = (
                result["friction_types"].get(ftype, 0) + count
            )

        # Session types
        stype = data.get("session_type", "unknown")
        result["session_types"][stype] = result["session_types"].get(stype, 0) + 1

        # Outcomes
        outcome = data.get("outcome", "unknown")
        result["outcomes"][outcome] = result["outcomes"].get(outcome, 0) + 1

        # Brief summaries
        summary = data.get("brief_summary", "")
        if summary:
            result["summaries"].append(summary)

    return result


def parse_session_meta(meta_dir: Path) -> dict:
    """Aggregate data from session-meta/*.json files.

    Returns dict with tool_counts, languages, session_count, date_range.
    """
    result: dict = {
        "total_sessions": 0,
        "total_messages": 0,
        "tool_counts": {},
        "languages": {},
        "date_range_start": "",
        "date_range_end": "",
    }

    if not meta_dir.exists():
        return result

    dates: list[str] = []

    for f in sorted(meta_dir.glob("*.json")):
        try:
            data = json.loads(f.read_text(encoding="utf-8"))
        except (json.JSONDecodeError, OSError):
            continue

        result["total_sessions"] += 1
        result["total_messages"] += data.get("user_message_count", 0)
        result["total_messages"] += data.get("assistant_message_count", 0)

        # Tool counts
        for tool, count in data.get("tool_counts", {}).items():
            result["tool_counts"][tool] = result["tool_counts"].get(tool, 0) + count

        # Languages
        for lang, count in data.get("languages", {}).items():
            result["languages"][lang] = result["languages"].get(lang, 0) + count

        # Date tracking
        start_time = data.get("start_time", "")
        if start_time:
            dates.append(start_time[:10])  # YYYY-MM-DD

    if dates:
        dates.sort()
        result["date_range_start"] = dates[0]
        result["date_range_end"] = dates[-1]

    return result


