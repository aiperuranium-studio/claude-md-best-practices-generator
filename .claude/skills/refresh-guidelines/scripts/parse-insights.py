def _build_entries(
    sections: dict[str, list[dict]],
    claude_md_recs: list[dict],
) -> list[dict]:
    """Convert parsed HTML sections into output entries."""
    entries: list[dict] = []

    # 1. CLAUDE.md Recommendations (highest internal priority)
    if claude_md_recs:
        blocks = []
        for rec in claude_md_recs:
            blocks.append({
                "theme": "behavioral",
                "heading": _extract_heading(rec["text"]),
                "text": rec["text"],
                "evidence": rec.get("evidence", ""),
            })
        entries.append({
            "source_id": "insights-claudemd-rec",
            "source_name": "/insights CLAUDE.md Recommendations",
            "tier": "insights",
            "themes": ["content", "behavioral"],
            "content_blocks": blocks,
        })

    # 2. Friction points
    if sections.get("friction"):
        blocks = []
        for item in sections["friction"]:
            blocks.append({
                "theme": "anti-patterns",
                "heading": item["title"],
                "text": item["description"],
                "evidence": item.get("evidence", ""),
            })
        entries.append({
            "source_id": "insights-friction",
            "source_name": "/insights Friction Points",
            "tier": "insights",
            "themes": ["anti-patterns", "behavioral"],
            "content_blocks": blocks,
        })

    # 3. Working workflows (wins)
    if sections.get("wins"):
        blocks = []
        for item in sections["wins"]:
            blocks.append({
                "theme": "behavioral",
                "heading": item["title"],
                "text": item["description"],
            })
        entries.append({
            "source_id": "insights-wins",
            "source_name": "/insights Working Workflows",
            "tier": "insights",
            "themes": ["behavioral"],
            "content_blocks": blocks,
        })

    # 4. Feature recommendations
    if sections.get("features"):
        blocks = []
        for item in sections["features"]:
            text = item["description"]
            if item.get("one_liner"):
                text = f"{item['one_liner']}\n\n{text}"
            blocks.append({
                "theme": "integration",
                "heading": item["title"],
                "text": text,
            })
        entries.append({
            "source_id": "insights-features",
            "source_name": "/insights Feature Recommendations",
            "tier": "insights",
            "themes": ["integration"],
            "content_blocks": blocks,
        })

    # 5. Usage patterns
    if sections.get("patterns"):
        blocks = []
        for item in sections["patterns"]:
            text = item["description"]
            if item.get("summary"):
                text = f"{item['summary']}\n\n{text}"
            blocks.append({
                "theme": "behavioral",
                "heading": item["title"],
                "text": text,
            })
        entries.append({
            "source_id": "insights-patterns",
            "source_name": "/insights Usage Patterns",
            "tier": "insights",
            "themes": ["behavioral", "maintenance"],
            "content_blocks": blocks,
        })

    # 6. Future workflows (horizon)
    if sections.get("horizon"):
        blocks = []
        for item in sections["horizon"]:
            text = item["description"]
            if item.get("tip"):
                text += f"\n\nGetting started: {item['tip']}"
            blocks.append({
                "theme": "maintenance",
                "heading": item["title"],
                "text": text,
            })
        entries.append({
            "source_id": "insights-horizon",
            "source_name": "/insights Future Workflows",
            "tier": "insights",
            "themes": ["maintenance"],
            "content_blocks": blocks,
        })

    # 7. Usage narrative
    if sections.get("usage"):
        blocks = []
        for item in sections["usage"]:
            blocks.append({
                "theme": "behavioral",
                "heading": item["title"],
                "text": item["description"],
            })
        entries.append({
            "source_id": "insights-usage",
            "source_name": "/insights Usage Narrative",
            "tier": "insights",
            "themes": ["behavioral"],
            "content_blocks": blocks,
        })

    return entries


def _extract_heading(text: str) -> str:
    """Extract a short heading from a CLAUDE.md recommendation text."""
    # Try to get the "Add under ..." prefix
    match = re.match(r"Add under (?:a new |the )?(##?\s*\S.*?)(?:\n|$)", text)
    if match:
        return match.group(1).strip()
    # Fallback: first ~60 chars
    first_line = text.split("\n")[0]
    if len(first_line) > 60:
        return first_line[:57] + "..."
    return first_line


def _parse_subtitle(subtitle: str) -> tuple[int, str]:
    """Extract session count and date range from subtitle text.

    Example: '134 messages across 19 sessions (24 total) | 2026-01-16 to 2026-02-10'
    Returns: (19, '2026-01-16 to 2026-02-10')
    """
    sessions = 0
    date_range = ""

    session_match = re.search(r"(\d+)\s+sessions", subtitle)
    if session_match:
        sessions = int(session_match.group(1))

    date_match = re.search(r"(\d{4}-\d{2}-\d{2}\s+to\s+\d{4}-\d{2}-\d{2})", subtitle)
    if date_match:
        date_range = date_match.group(1)

    return sessions, date_range


