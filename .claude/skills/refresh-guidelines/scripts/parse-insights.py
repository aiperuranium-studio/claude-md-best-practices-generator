def main(report_dir: Path | None = None) -> int:
    """Parse /insights data and write structured output.

    Args:
        report_dir: Override for ~/.claude/usage-data/ (for testing).

    Returns 0 on success (including missing report), 1 on fatal error.
    """
    data_dir = report_dir or DEFAULT_USAGE_DATA_DIR

    print(
        f"parse-insights.py — {datetime.now(timezone.utc).isoformat()}",
        file=sys.stderr,
    )

    # Find report
    report_path, staleness_days = find_report(data_dir)

    if report_path is None:
        print(
            f"WARNING: No /insights report found at {data_dir / 'report.html'}",
            file=sys.stderr,
        )
        print("Producing minimal output — web sources only.", file=sys.stderr)
        _write_minimal_output(data_dir)
        return 0

    is_stale = staleness_days > STALENESS_DAYS
    if is_stale:
        print(
            f"WARNING: /insights report is {staleness_days} days old (stale > {STALENESS_DAYS} days).",
            file=sys.stderr,
        )
        print(
            "Consider running /insights first for fresher data.",
            file=sys.stderr,
        )

    print(f"Report: {report_path} (age: {staleness_days} days)", file=sys.stderr)

    # Parse HTML report
    try:
        html_content = report_path.read_text(encoding="utf-8")
    except OSError as e:
        print(f"ERROR: Cannot read report: {e}", file=sys.stderr)
        _write_minimal_output(data_dir)
        return 0

    parser = InsightsHTMLParser()
    try:
        parser.feed(html_content)
    except Exception as e:
        print(f"WARNING: HTML parsing error: {e}", file=sys.stderr)

    # Parse supporting JSON
    facets_dir = data_dir / "facets"
    meta_dir = data_dir / "session-meta"

    facets_data = parse_facets(facets_dir)
    meta_data = parse_session_meta(meta_dir)

    # Build output entries
    entries = _build_entries(parser.sections, parser.claude_md_recs)

    # Extract metadata from subtitle
    sessions_from_subtitle, date_range = _parse_subtitle(parser.subtitle)

    # Report modification time
    report_mtime = datetime.fromtimestamp(
        report_path.stat().st_mtime, tz=timezone.utc
    ).isoformat()

    # Compose output
    output = {
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "report_path": str(report_path),
        "report_modified_at": report_mtime,
        "staleness_days": staleness_days,
        "is_stale": is_stale,
        "sessions_analyzed": sessions_from_subtitle or meta_data["total_sessions"],
        "date_range": date_range or (
            f"{meta_data['date_range_start']} to {meta_data['date_range_end']}"
            if meta_data["date_range_start"]
            else ""
        ),
        "entries": entries,
        "session_stats": {
            "total_sessions": meta_data["total_sessions"],
            "total_messages": meta_data["total_messages"],
            "top_tools": dict(
                sorted(
                    meta_data["tool_counts"].items(),
                    key=lambda x: x[1],
                    reverse=True,
                )[:6]
            ),
            "primary_languages": dict(
                sorted(
                    meta_data["languages"].items(),
                    key=lambda x: x[1],
                    reverse=True,
                )[:6]
            ),
            "outcome_distribution": facets_data["outcomes"],
        },
    }

    # Write output
    OUTPUT_FILE.parent.mkdir(parents=True, exist_ok=True)
    OUTPUT_FILE.write_text(
        json.dumps(output, indent=2, ensure_ascii=False) + "\n",
        encoding="utf-8",
    )

    # Summary
    total_blocks = sum(len(e["content_blocks"]) for e in entries)
    print("\n--- Summary ---", file=sys.stderr)
    print(f"Entries: {len(entries)} sections, {total_blocks} content blocks", file=sys.stderr)
    print(f"Sessions analyzed: {output['sessions_analyzed']}", file=sys.stderr)
    print(f"Date range: {output['date_range']}", file=sys.stderr)
    print(f"Stale: {is_stale} ({staleness_days} days)", file=sys.stderr)
    print(f"Output: {OUTPUT_FILE}", file=sys.stderr)

    return 0


